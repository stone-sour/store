package com.kdstore.product;

public class JewleryProduct extends Product {
	
	private String jewleryType;
	private double jewlerySize;
	
	public String getJewleryType() {
		return jewleryType;
	}
	public void setJewleryType(String jewleryType) {
		this.jewleryType = jewleryType;
	}
	public double getJewlerySize() {
		return jewlerySize;
	}
	public void setJewlerySize(double jewlerySize) {
		this.jewlerySize = jewlerySize;
	}

}
