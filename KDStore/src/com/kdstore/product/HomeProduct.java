package com.kdstore.product;

public class HomeProduct extends Product{
	
	private String homeProductType;
	private String homeProductDescription;
	
	public String getHomeProductType() {
		return homeProductType;
	}
	public void setHomeProductType(String homeProductType) {
		this.homeProductType = homeProductType;
	}
	public String getHomeProductDescription() {
		return homeProductDescription;
	}
	public void setHomeProductDescription(String homeProductDescription) {
		this.homeProductDescription = homeProductDescription;
	}

}
