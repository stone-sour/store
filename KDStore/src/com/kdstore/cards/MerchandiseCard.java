package com.kdstore.cards;

public class MerchandiseCard {
	
	private int merchCardId;
	private int merchReturnId;
	private double merchBalance;
	
	public int getMerchCardId() {
		return merchCardId;
	}
	public void setMerchCardId(int merchCardId) {
		this.merchCardId = merchCardId;
	}
	public int getMerchReturnId() {
		return merchReturnId;
	}
	public void setMerchReturnId(int merchReturnId) {
		this.merchReturnId = merchReturnId;
	}
	public double getMerchBalance() {
		return merchBalance;
	}
	public void setMerchBalance(double merchBalance) {
		this.merchBalance = merchBalance;
	}

}
